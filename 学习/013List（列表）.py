# 2、怎么创建 List（列表）
# 从上面的例子可以分析出，列表的格式是这样的。

# 其实列表就是用中括号 [] 括起来的数据，里面的每一个数据就叫做元素。每个元素之间使用逗号分隔。
#
# 而且列表的数据元素不一定是相同的数据类型。
